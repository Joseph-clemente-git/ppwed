export const municipalityPerformance = municipalities.map(m => {
  const munProjects = projects.filter(p => p.municipalityId === m.id);
  const totalBudget = munProjects.reduce((s, p) => s + p.contractAmount, 0);
  const avgAccomplishment = munProjects.length > 0
    ? Math.round(munProjects.reduce((s, p) => s + p.actualProgress, 0) / munProjects.length)
    : 0;
  const atRisk = munProjects.filter(p => p.riskLevel === 'high' || p.riskLevel === 'critical').length;
  return {
    municipalityId: m.id,
    name: m.name,
    totalProjects: munProjects.length,
    totalBudget,
    avgAccomplishment,
    atRisk,
  };
});

